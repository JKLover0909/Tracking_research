def yaml_value(value):
    if isinstance(value, bool):
        return "True" if value else "False"
    if value is None:
        return "None"
    return str(value)


def tracker_yaml(tracker_type):
    if tracker_type not in {"bytetrack", "botsort"}:
        raise ValueError(f"Unsupported tracker_type: {tracker_type}")

    params = {"tracker_type": tracker_type, **COMMON_TRACKER_PARAMS}
    if tracker_type == "botsort":
        params.update(BOTSORT_EXTRA_PARAMS)

    lines = [
        "# Generated by src/difference.py",
        "# Common params are identical for ByteTrack and BoT-SORT.",
    ]
    if tracker_type == "botsort":
        lines.append("# BoT-SORT-only ReID/GMC features are enabled below.")
    else:
        lines.append("# ByteTrack has no ReID/GMC block in Ultralytics.")
    lines.extend(f"{key}: {yaml_value(value)}" for key, value in params.items())
    return "\n".join(lines) + "\n"


def write_tracker_cfgs(tmp_dir):
    cfgs = {}
    for name in ("bytetrack", "botsort"):
        path = Path(tmp_dir) / f"{name}.yaml"
        path.write_text(tracker_yaml(name), encoding="utf-8")
        cfgs[name] = str(path)
    return cfgs


def color_for(track_id):
    return PALETTE[int(track_id) % len(PALETTE)]


def draw_label(frame, text, x, y, color, font_scale=0.55):
    (tw, th), _ = cv2.getTextSize(text, cv2.FONT_HERSHEY_SIMPLEX, font_scale, 1)
    x = max(0, min(x, frame.shape[1] - tw - 4))
    y = max(th + 8, min(y, frame.shape[0] - 2))
    cv2.rectangle(frame, (x, y - th - 8), (x + tw + 4, y), color, -1)
    cv2.putText(
        frame,
        text,
        (x + 2, y - 4),
        cv2.FONT_HERSHEY_SIMPLEX,
        font_scale,
        (0, 0, 0),
        1,
        cv2.LINE_AA,
    )


def draw_tracks(frame, result, title, frame_idx, fps_val, seen_ids):
    panel = frame.copy()
    active_ids = set()

    if result.boxes is not None and result.boxes.id is not None:
        boxes = result.boxes.xyxy.detach().cpu().numpy()
        ids = result.boxes.id.int().detach().cpu().numpy()
        confs = result.boxes.conf.detach().cpu().numpy()

        for box, track_id, conf in zip(boxes, ids, confs):
            track_id = int(track_id)
            active_ids.add(track_id)
            seen_ids.add(track_id)
            x1, y1, x2, y2 = [int(v) for v in box]
            color = color_for(track_id)
            cv2.rectangle(panel, (x1, y1), (x2, y2), color, 2, cv2.LINE_AA)
            draw_label(panel, f"ID:{track_id} c:{conf:.2f}", x1, y1, color)

    overlay = panel.copy()
    cv2.rectangle(overlay, (0, 0), (430, 116), (20, 20, 20), -1)
    cv2.addWeighted(overlay, 0.65, panel, 0.35, 0, panel)
    lines = [
        title,
        f"Frame: {frame_idx}",
        f"FPS: {fps_val:.1f}",
        f"Active raw IDs: {len(active_ids)}",
        f"Total unique raw IDs: {len(seen_ids)}",
    ]
    for idx, line in enumerate(lines):
        font_scale = 0.70 if idx == 0 else 0.55
        color = (240, 240, 240) if idx == 0 else (200, 240, 200)
        cv2.putText(
            panel,
            line,
            (10, 24 + idx * 21),
            cv2.FONT_HERSHEY_SIMPLEX,
            font_scale,
            color,
            1,
            cv2.LINE_AA,
        )
    return panel, active_ids


def resize_panel(frame, scale):
    if abs(scale - 1.0) < 1e-6:
        return frame
    width = max(1, int(frame.shape[1] * scale))
    height = max(1, int(frame.shape[0] * scale))
    return cv2.resize(frame, (width, height), interpolation=cv2.INTER_AREA)


def run_tracker(model, frame, tracker_cfg, args, yolo_device, use_half):
    return model.track(
        source=frame,
        persist=True,
        tracker=tracker_cfg,
        classes=[0],
        conf=args.conf,
        iou=args.iou,
        imgsz=args.imgsz,
        half=use_half,
        device=yolo_device,
        verbose=False,
    )[0]


def draw_bar_chart(canvas, origin, size, labels, values, colors, title, lower_is_better):
    x0, y0 = origin
    width, height = size
    chart_pad = 44
    plot_x = x0 + chart_pad
    plot_y = y0 + 74
    plot_w = width - chart_pad * 2
    plot_h = height - 134
    max_value = max(max(values), 1.0)

    cv2.rectangle(canvas, (x0, y0), (x0 + width, y0 + height), (255, 255, 255), -1)
    cv2.rectangle(canvas, (x0, y0), (x0 + width, y0 + height), (217, 226, 236), 2)
    cv2.putText(
        canvas,
        title,
        (x0 + 24, y0 + 38),
        cv2.FONT_HERSHEY_SIMPLEX,
        0.78,
        (31, 41, 51),
        2,
        cv2.LINE_AA,
    )
    cv2.putText(
        canvas,
        "Lower is better" if lower_is_better else "Higher is better",
        (x0 + 24, y0 + 64),
        cv2.FONT_HERSHEY_SIMPLEX,
        0.48,
        (82, 96, 109),
        1,
        cv2.LINE_AA,
    )

    cv2.line(canvas, (plot_x, plot_y + plot_h), (plot_x + plot_w, plot_y + plot_h), (203, 210, 217), 2)
    bar_w = int(plot_w * 0.22)
    gap = int(plot_w * 0.22)
    start_x = plot_x + int(plot_w * 0.18)
    for idx, (label, value, color) in enumerate(zip(labels, values, colors)):
        bar_h = int((value / max_value) * max(plot_h - 20, 1))
        bx = start_x + idx * (bar_w + gap)
        by = plot_y + plot_h - bar_h
        cv2.rectangle(canvas, (bx, by), (bx + bar_w, plot_y + plot_h), color, -1)
        cv2.putText(
            canvas,
            f"{value:.1f}" if isinstance(value, float) and not value.is_integer() else str(int(value)),
            (bx + 6, max(by - 12, plot_y + 18)),
            cv2.FONT_HERSHEY_SIMPLEX,
            0.72,
            (31, 41, 51),
            2,
            cv2.LINE_AA,
        )
        cv2.putText(
            canvas,
            label,
            (bx - 10, plot_y + plot_h + 32),
            cv2.FONT_HERSHEY_SIMPLEX,
            0.58,
            (31, 41, 51),
            2,
            cv2.LINE_AA,
        )


