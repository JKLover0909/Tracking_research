def extract_tracks(result):
    tracks = []
    if result.boxes is None or result.boxes.id is None:
        return tracks

    boxes = result.boxes.xyxy.detach().cpu().numpy()
    ids = result.boxes.id.int().detach().cpu().numpy()
    confs = result.boxes.conf.detach().cpu().numpy()
    for box, track_id, conf in zip(boxes, ids, confs):
        tracks.append({
            "id": int(track_id),
            "box": [float(v) for v in box],
            "conf": float(conf),
        })
    return tracks


class TrackerStats:
    def __init__(self, name, short_track_frames=15):
        self.name = name
        self.short_track_frames = short_track_frames
        self.frames = 0
        self.seen_ids = set()
        self.prev_active_ids = set()
        self.active_counts = []
        self.detection_counts = []
        self.latencies = []
        self.conf_values = []
        self.new_id_events = 0
        self.lost_id_events = 0
        self.reappeared_id_events = 0
        self.track_frames = collections.defaultdict(int)
        self.track_first_frame = {}
        self.track_last_frame = {}
        self.track_gap_events = collections.defaultdict(int)

    def update(self, frame_idx, tracks, latency_sec):
        active_ids = {track["id"] for track in tracks}
        new_ids = active_ids - self.seen_ids
        lost_ids = self.prev_active_ids - active_ids
        reappeared_ids = (active_ids & self.seen_ids) - self.prev_active_ids

        self.frames += 1
        self.new_id_events += len(new_ids)
        self.lost_id_events += len(lost_ids)
        self.reappeared_id_events += len(reappeared_ids)
        self.active_counts.append(len(active_ids))
        self.detection_counts.append(len(tracks))
        self.latencies.append(float(latency_sec))

        for track in tracks:
            track_id = track["id"]
            self.seen_ids.add(track_id)
            self.track_frames[track_id] += 1
            self.track_first_frame.setdefault(track_id, frame_idx)
            previous_last = self.track_last_frame.get(track_id)
            if previous_last is not None and frame_idx - previous_last > 1:
                self.track_gap_events[track_id] += 1
            self.track_last_frame[track_id] = frame_idx
            self.conf_values.append(track["conf"])

        self.prev_active_ids = active_ids

    def _mean(self, values):
        return float(np.mean(values)) if values else 0.0

    def _median(self, values):
        return float(np.median(values)) if values else 0.0

    def _p95(self, values):
        return float(np.percentile(values, 95)) if values else 0.0

    def summary(self):
        track_lengths = list(self.track_frames.values())
        avg_active = self._mean(self.active_counts)
        unique_ids = len(self.seen_ids)
        total_latency = sum(self.latencies)
        return {
            "name": self.name,
            "frames": self.frames,
            "unique_ids": unique_ids,
            "avg_active_ids": avg_active,
            "max_active_ids": max(self.active_counts) if self.active_counts else 0,
            "avg_detections_per_frame": self._mean(self.detection_counts),
            "new_id_events": self.new_id_events,
            "lost_id_events": self.lost_id_events,
            "reappeared_id_events": self.reappeared_id_events,
            "gap_events": int(sum(self.track_gap_events.values())),
            "short_tracks": int(sum(1 for length in track_lengths if length <= self.short_track_frames)),
            "avg_track_observed_frames": self._mean(track_lengths),
            "median_track_observed_frames": self._median(track_lengths),
            "avg_confidence": self._mean(self.conf_values),
            "avg_fps": self.frames / max(total_latency, 1e-6),
            "avg_latency_ms": self._mean(self.latencies) * 1000.0,
            "median_latency_ms": self._median(self.latencies) * 1000.0,
            "p95_latency_ms": self._p95(self.latencies) * 1000.0,
            "id_fragmentation_proxy": unique_ids / max(avg_active, 1e-6),
        }

    def track_rows(self):
        rows = []
        for track_id in sorted(self.track_frames):
            rows.append({
                "tracker": self.name,
                "raw_id": track_id,
                "first_frame": self.track_first_frame.get(track_id, ""),
                "last_frame": self.track_last_frame.get(track_id, ""),
                "observed_frames": self.track_frames[track_id],
                "gap_events": self.track_gap_events.get(track_id, 0),
            })
        return rows


